package latihan3.field;

/*
    FIELD
    - fields/properties/attributes adalah data yang bisa kita sisipkan di dalam object
    - namun sebelum kita bisa memasukkan data di fields, kita harus mendeklarasikan data
      apa aja yang dimiliki object tersebut di dalam deklarasi class nya
    - membuat field sama seperti membuat variabel, namun ditempatkan di block class
 */

class Person {
    //fields
    String name;
    String address;
    final String country = "Indonesia";
}
